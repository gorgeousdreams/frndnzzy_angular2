export class Student {
    student_id: number;
    mobile_number: string;
    email: string;
    user_name: string;
    user_id: number;
    constructor(student_id, mobile_number, email, user_name, user_id) {
        student_id = student_id;
        mobile_number = mobile_number;
        email = email;
        user_name = user_name;
        user_id = user_id;
    }

}